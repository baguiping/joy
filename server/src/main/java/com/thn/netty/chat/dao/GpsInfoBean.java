package com.thn.netty.chat.dao;

public class GpsInfoBean {
	String imei;
	String latitude;
	String latitudeDivision;
	String longitude;
	String longitudeDivision;
	
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLatitudeDivision() {
		return latitudeDivision;
	}
	public void setLatitudeDivision(String latitudeDivision) {
		this.latitudeDivision = latitudeDivision;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLongitudeDivision() {
		return longitudeDivision;
	}
	public void setLongitudeDivision(String longitudeDivision) {
		this.longitudeDivision = longitudeDivision;
	}

}
