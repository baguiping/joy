package com.thn.netty.chat.dao;

public class ParamCfgBean {
	int cfgid;
	String  imei;
	int token;
	int cmd;
	String value;
	String   tm;
	int flag;
	public int getCfgid() {
		return cfgid;
	}
	public void setCfgid(int cfgid) {
		this.cfgid = cfgid;
	}
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public int getToken() {
		return token;
	}
	public void setToken(int token) {
		this.token = token;
	}
	public int getCmd() {
		return cmd;
	}
	public void setCmd(int cmd) {
		this.cmd = cmd;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getTm() {
		return tm;
	}
	public void setTm(String tm) {
		this.tm = tm;
	}
	public int getFlag() {
		return flag;
	}
	public void setFlag(int flag) {
		this.flag = flag;
	}
	
	

}
